(test/deftest read-maze-1
  (test/is (= {:path
               #{[3 15] [13 3] [13 15] [17 5] [2 8] [17 6] [12 13] [15 3] [5 10]
                 [17 12] [9 6] [11 13] [9 3] [4 10] [3 13] [4 9] [2 13] [16 3]
